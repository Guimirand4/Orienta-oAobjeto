package biblioteca;

public class Autor {
	private int id;
	private String nome, generoLiteraio;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getGeneroLiteraio() {
		return generoLiteraio;
	}
	public void setGeneroLiteraio(String generoLiteraio) {
		this.generoLiteraio = generoLiteraio;
	}
	
	
}
